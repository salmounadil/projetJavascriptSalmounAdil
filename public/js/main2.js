// afficher masquer cartes

//1er carte

var visible1 = document.getElementById('divvisible1');

var hiddenDiv1 = document.getElementById('visible1');


visible1.addEventListener('mouseover', function handleMouseOver() {
  hiddenDiv1.style.display = 'none';
});

visible1.addEventListener('mouseout', function handleMouseOut() {
    hiddenDiv1.style.display = 'block';
});

